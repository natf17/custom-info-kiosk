CREATE TABLE IF NOT EXISTS KioskLocale (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY, 
    abbrev VARCHAR(10), 
    name VARCHAR(50),
    kioskPageInternals INTEGER
);

CREATE TABLE IF NOT EXISTS PageMetadata (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    createdOn DATE,
    lastModified TIMESTAMP,
    kioskPageInternals INTEGER
);

CREATE TABLE IF NOT EXISTS KioskPageInternals (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    aboutPage INTEGER,,
);

CREATE TABLE IF NOT EXISTS RDescriptionField (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    fieldName VARCHAR(50),
    fieldValue VARCHAR(800),
    aboutPage INTEGER,
);

CREATE TABLE IF NOT EXISTS Image (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    location VARCHAR(50),
    width INTEGER,
    height INTEGER,
    imageField INTEGER
);

CREATE TABLE IF NOT EXISTS AlternateTextField (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    fieldName VARCHAR(50),
    fieldValue VARCHAR(100),
    imageField INTEGER
);

CREATE TABLE IF NOT EXISTS ImageField (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    fieldName VARCHAR(50),
);

CREATE TABLE IF NOT EXISTS PageTitleField (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    fieldName VARCHAR(50),
    fieldValue VARCHAR(100),
    aboutPage INTEGER
);

CREATE TABLE IF NOT EXISTS AboutPage (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
);




